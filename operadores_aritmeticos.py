adicao = 1+1 # 1+1 = 2
subtracao = 1-1 # 1-1 = 0
multiplicacao = 2*2 # 2*2 = 4
divisao = 10/2 # 10/2 = 5.0 (float)
potenciacao = 2**3 #2^3 = 8
divisao_inteira = 10//3 # 10/3 = 3.3333333333333335 = 3
resto_divisao = 10%3 # 10/3 = 3.3333333333333335 = 1
print('Adição:', adicao) 
print('Subtração:', subtracao)
print('Multiplicação:', multiplicacao)
print('Divisão:', divisao)
print('Potenciação:', potenciacao)
print('Divisão inteira:', divisao_inteira)
print('Resto da divisão:', resto_divisao)

